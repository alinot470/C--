// Write a program to calculate area of circle?
#include <iostream>
using namespace std;
int main()
{
  float are_of_circle, radious, pay;
  // Area of cricle is pay(radios)square
  pay = 3.14;
  cout << "Enter radious of cricle :";
  cin >> radious;
  are_of_circle = (pay * (radious * radious));
  cout << "Area of cricle is :" << are_of_circle;
}
